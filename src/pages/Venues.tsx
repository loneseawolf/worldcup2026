/** map label: full city name when it fits, else first words within ~13 chars */
function shortCity(name: string, venueId: string, lang: string): string {
  const override = LABEL_OVERRIDE[venueId]?.[lang]
  if (override) return override
  if (name.length <= 13) return name
  const words = name.split(/\s+/)
  const two = words.slice(0, 2).join(' ')
  return two.length <= 13 ? two : words[0]
}

/** team picker with real flag images (native <option> can't render components) */
function TeamDropdown({
  teams,
  value,
  onChange,
  allLabel,
  label,
}: {
  teams: Team[]
  value: string | null
  onChange: (code: string | null) => void
  allLabel: string
  label: string
}) {
  const { pick } = useI18n()
  const [open, setOpen] = useState(false)
  const ref = useRef<HTMLDivElement>(null)

  useEffect(() => {
    if (!open) return
    const close = (e: MouseEvent) => {
      if (ref.current && !ref.current.contains(e.target as Node)) setOpen(false)
    }
    const esc = (e: KeyboardEvent) => {
      if (e.key === 'Escape') setOpen(false)
    }
    document.addEventListener('mousedown', close)
    document.addEventListener('keydown', esc)
    return () => {
      document.removeEventListener('mousedown', close)
      document.removeEventListener('keydown', esc)
    }
  }, [open])

  const selected = value ? teams.find((tm) => tm.code === value) : null

  const choose = (code: string | null) => {
    onChange(code)
    setOpen(false)
  }

  return (
    <div className={`vn-dd${open ? ' open' : ''}${value ? ' has-sel' : ''}`} ref={ref}>
      <button
        type="button"
        className="vn-dd-btn"
        aria-haspopup="listbox"
        aria-expanded={open}
        aria-label={label}
        onClick={() => setOpen((o) => !o)}
      >
        {selected ? (
          <>
            <Flag team={selected} size={18} />
            <span className="vn-dd-name">{pick(selected.name, selected.code)}</span>
          </>
        ) : (
          <span className="vn-dd-name">{allLabel}</span>
        )}
        <Icon name="chevron" size={14} />
      </button>
      {value && (
        <button
          type="button"
          className="vn-dd-clear"
          aria-label={allLabel}
          title={allLabel}
          onClick={() => choose(null)}
        >
          <Icon name="close" size={13} />
        </button>
      )}
      {open && (
        <div className="vn-dd-list" role="listbox" aria-label={label}>
          <button type="button" role="option" aria-selected={value === null} onClick={() => choose(null)}>
            <span className="vn-dd-spacer" />
            {allLabel}
          </button>
          {teams.map((tm) => (
            <button
              type="button"
              key={tm.code}
              role="option"
              aria-selected={value === tm.code}
              onClick={() => choose(tm.code)}
            >
              <Flag team={tm} size={18} />
              {pick(tm.name, tm.code)}
            </button>
          ))}
        </div>
      )}
    </div>
  )
}

function VenueMap({
  venues,
  counts,
  camps,
  selTeam,
  selVenueIds,
  selMatchCount,
  flashVenue,
  onSelTeam,
  onPin,
  onCountry,
}: {
  venues: Venue[]
  counts: Record<HostCountry, number>
  camps: Team[]
  selTeam: string | null
  selVenueIds: ReadonlySet<string>
  selMatchCount: number
  flashVenue: string | null
  onSelTeam: (code: string | null) => void
  onPin: (id: string) => void
  onCountry: (c: HostCountry) => void
}) {
  const { t, pick, lang, locale } = useI18n()
  const navigate = useNavigate()
  const [showCamps, setShowCamps] = useState(true)

  // crop horizontal whitespace: hug the pins/labels/camps extent (the coasts),
  // which also ignores off-frame slivers like the Alaska fragment
  const contentRef = useRef<SVGGElement>(null)
  const [crop, setCrop] = useState<[number, number, number, number] | null>(null)
  useLayoutEffect(() => {
    const b = contentRef.current?.getBBox()
    if (b && b.width > 0) {
      const pad = 14
      const x0 = Math.max(Math.floor(b.x - pad), 0)
      const x1 = Math.min(Math.ceil(b.x + b.width + pad), MAP_W)
      const y0 = Math.max(Math.floor(b.y - pad), 0)
      const y1 = Math.min(Math.ceil(b.y + b.height + pad), MAP_H)
      setCrop([x0, y0, x1 - x0, y1 - y0])
    }
  }, [])

  const teamOptions = useMemo(
    () => camps.slice().sort((a, b) => pick(a.name, a.code).localeCompare(pick(b.name, b.code), locale)),
    [camps, pick, locale],
  )

  // project the camps, then run a deterministic collision-relaxation pass so no
  // flag overlaps another flag or a stadium pin (same-city camps fan out around it)
  const campPins = useMemo(() => {
    const venuePts = venues.map((v) => project(v.lat, v.lon))
    // city-label bounding boxes are obstacles too (12px font ≈ 6.6px/char)
    const labelRects = venues.map((v) => {
      const { x, y } = project(v.lat, v.lon)
      const tw = LABEL_TWEAK[v.id] ?? {}
      const anchor = tw.anchor ?? 'start'
      const dx = tw.dx ?? 11
      const dy = tw.dy ?? 4
      const w = shortCity(pick(v.cityName, v.city), v.id, lang).length * 6.6
      const x0 = anchor === 'start' ? x + dx : x + dx - w
      return { x0, x1: x0 + w, y0: y + dy - 11, y1: y + dy + 4 }
    })
    const pts = camps
      .flatMap((tm) => {
        const bc = tm.baseCamp
        return bc && bc.lat != null && bc.lon != null ? [{ tm, ...project(bc.lat, bc.lon) }] : []
      })
      .sort((a, b) => a.tm.code.localeCompare(b.tm.code))
    const W = 19 // min horizontal separation (flag is 17 wide)
    const H = 15 // min vertical separation (flag is 13 tall)
    for (let iter = 0; iter < 80; iter++) {
      let moved = false
      for (const p of pts) {
        for (const v of venuePts) {
          const dx = p.x - v.x
          const dy = p.y - v.y
          if (Math.abs(dx) < 17 && Math.abs(dy) < 15) {
            p.y += dy >= 0 ? 4 : -4 // slide off the stadium pin vertically
            moved = true
          }
        }
        for (const r of labelRects) {
          if (p.x + 9.5 > r.x0 && p.x - 9.5 < r.x1 && p.y + 7.5 > r.y0 && p.y - 7.5 < r.y1) {
            p.y += p.y >= (r.y0 + r.y1) / 2 ? 4 : -4 // slide off the city label
            moved = true
          }
        }
      }
      for (let i = 0; i < pts.length; i++) {
        for (let j = i + 1; j < pts.length; j++) {
          const a = pts[i]
          const b = pts[j]
          const dx = b.x - a.x
          const dy = b.y - a.y
          if (Math.abs(dx) < W && Math.abs(dy) < H) {
            const px = (W - Math.abs(dx)) / 2
            const py = (H - Math.abs(dy)) / 2
            if (px <= py) {
              const sgn = dx >= 0 ? 1 : -1
              a.x -= sgn * px
              b.x += sgn * px
            } else {
              const sgn = dy !== 0 ? (dy > 0 ? 1 : -1) : i % 2 ? 1 : -1
              a.y -= sgn * py
              b.y += sgn * py
            }
            moved = true
          }
        }
      }
      if (!moved) break
    }
    for (const p of pts) {
      p.x = Math.min(Math.max(p.x, 10), MAP_W - 10)
      p.y = Math.min(Math.max(p.y, 8), MAP_H - 8)
    }
    return pts
  }, [camps, venues, pick, lang])

  return (
    <div className="card vn-map-card">
      <div className="vn-map-wrap">
        <svg
          className="vn-svg"
          viewBox={crop ? crop.join(' ') : `0 0 ${MAP_W} ${MAP_H}`}
          role="group"
          aria-label={t('venuesSub')}
        >
          <path className="vn-ctx" d={naMap.context} />
          {COUNTRIES.map((c) => (
            <path key={c} className={`vn-country ${COUNTRY_CLASS[c]}`} d={naMap.countries[c]} />
          ))}
          <path className="vn-lakes" d={naMap.lakes} />

          <g ref={contentRef}>
            {showCamps &&
              campPins.map(({ tm, x, y }) => (
                <g
                  key={tm.code}
                  className={`vn-camp${selTeam && tm.code !== selTeam ? ' vn-dim' : ''}${selTeam === tm.code ? ' vn-camp-sel' : ''}`}
                  transform={`translate(${x.toFixed(1)} ${y.toFixed(1)})`}
                  role="button"
                  tabIndex={0}
                  aria-label={`${t('baseCamp')}: ${pick(tm.name, tm.code)}`}
                  onClick={() => navigate(`/team/${tm.code}`)}
                  onKeyDown={(e) => {
                    if (e.key === 'Enter' || e.key === ' ') {
                      e.preventDefault()
                      navigate(`/team/${tm.code}`)
                    }
                  }}
                >
                  <title>{`${pick(tm.name, tm.code)} · ${tm.baseCamp?.facility || tm.baseCamp?.city || ''}`}</title>
                  <rect x={-8.5} y={-6.5} width={17} height={13} rx={1.5} className="vn-camp-bg" />
                  {tm.iso2 && (
                    <image
                      href={flagSrc(tm.iso2)}
                      x={-7.5}
                      y={-5.5}
                      width={15}
                      height={11}
                      preserveAspectRatio="xMidYMid meet"
                    />
                  )}
                </g>
              ))}

            {venues.map((v) => {
              const { x, y } = project(v.lat, v.lon)
              const tw = LABEL_TWEAK[v.id] ?? {}
              const anchor = tw.anchor ?? 'start'
              const dx = tw.dx ?? 11
              const dy = tw.dy ?? 4
              const color = COUNTRY_COLOR[v.country]
              const dim = selTeam !== null && !selVenueIds.has(v.id)
              return (
                <g
                  key={v.id}
                  className={`vn-pin${dim ? ' vn-dim' : ''}${flashVenue === v.id ? ' vn-flash' : ''}`}
                  transform={`translate(${x.toFixed(1)} ${y.toFixed(1)})`}
                  role="button"
                  tabIndex={0}
                  aria-label={v.realName}
                  onClick={() => onPin(v.id)}
                  onKeyDown={(e) => {
                    if (e.key === 'Enter' || e.key === ' ') {
                      e.preventDefault()
                      onPin(v.id)
                    }
                  }}
                >
                  <title>{v.realName}</title>
                  <circle className="vn-halo" r="12" fill={color} />
                  <circle className="vn-core" r="6.5" fill={color} stroke="#fff" strokeWidth="2" />
                  <text className="vn-lbl" x={dx} y={dy} textAnchor={anchor}>
                    {shortCity(pick(v.cityName, v.city), v.id, lang)}
                  </text>
                </g>
              )
            })}
          </g>
        </svg>
      </div>

      <div className="vn-legend">
        {COUNTRIES.map((c) => (
          <button key={c} type="button" className="vn-leg" onClick={() => onCountry(c)}>
            <span className="vn-dot" style={{ background: COUNTRY_COLOR[c] }} aria-hidden="true" />
            <strong>{t(HOST_KEY[c])}</strong>
            <span className="muted small tnum">{t('matchesShown', { n: counts[c] })}</span>
          </button>
        ))}
        <button
          type="button"
          className={`vn-leg vn-leg-camps${showCamps ? '' : ' off'}`}
          aria-pressed={showCamps}
          onClick={() => setShowCamps((v) => !v)}
        >
          <span className="vn-camp-glyph" aria-hidden="true" />
          <strong>{t('baseCamp')}</strong>
          <span className="muted small tnum">{campPins.length}</span>
        </button>
        <span className="vn-team-filter">
          <TeamDropdown
            teams={teamOptions}
            value={selTeam}
            onChange={onSelTeam}
            allLabel={t('allTeams')}
            label={t('filterTeams')}
          />
          {selTeam && (
            <span className="muted small vn-team-hint tnum">{t('matchesShown', { n: selMatchCount })}</span>
          )}
        </span>
      </div>
    </div>
  )
}

/* ================= match rows ================= */

function VenueMatchRow({ m, tz }: { m: Match; tz?: string }) {
  const { t, pick, locale } = useI18n()
  const { teams } = useAppData()
  const played = m.status === 'finished' || m.status === 'live'

  const side = (s: MatchSide | null, ph: string | null, right: boolean) => {
    const team = s ? teams[s.code] : null
    const label = s ? (team ? pick(team.name, s.code) : s.code) : ph ? placeholderLabel(ph, t) : t('tbd')
    return (
      <span className={`vn-mside${right ? ' right' : ''}`}>
        {!right && team && <Flag team={team} size={18} />}
        <span className={`vn-tname${team ? '' : ' tbd'}`}>{label}</span>
        {right && team && <Flag team={team} size={18} />}
      </span>
    )
  }

  return (
    <Link to={`/match/${m.id}`} className={`vn-mrow${m.status === 'live' ? ' live' : ''}`}>
      <span className="vn-mwhen small tnum">
        <b>{fmtDate(m.date, locale, tz)}</b>
        {m.status === 'live' ? (
          <span className="chip chip-live">{t('statusLive')}</span>
        ) : (
          <span className="muted">{fmtTime(m.date, locale, tz)}</span>
        )}
      </span>
      <span className="vn-mteams">
        {side(m.home, m.phA, false)}
        <span className={played && m.home && m.away ? 'vn-msc tnum' : 'vn-mvs muted small'}>
          {played && m.home && m.away ? `${m.home.score ?? '–'}–${m.away.score ?? '–'}` : t('vs')}
        </span>
        {side(m.away, m.phB, true)}
      </span>
      <span className="chip vn-mstage">
        {m.stage === 'group' && m.group ? t('groupX', { x: m.group }) : t(STAGE_LABEL_KEY[m.stage])}
      </span>
    </Link>
  )
}

