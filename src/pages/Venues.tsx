/* ================= SVG map =================
 * Real geography: Natural Earth 50m outlines projected with a Lambert conformal
 * conic (north-america standard), pre-generated by scripts/genmap.mjs. */

import naMap from '../data/na-map.json'

const MAP_W = naMap.w
const MAP_H = naMap.h

/** project lat/lon with the exact parameters the map paths were generated with */
function project(lat: number, lon: number): { x: number; y: number } {
  const { n, F, rho0, lon0, scale, tx, minY, pad } = naMap.proj
  const D = Math.PI / 180
  const rho = F / Math.tan(Math.PI / 4 + (lat * D) / 2) ** n
  const th = n * (lon - lon0) * D
  const px = rho * Math.sin(th)
  const py = rho0 - rho * Math.cos(th)
  return { x: px * scale + tx, y: MAP_H - pad - (py - minY) * scale }
}

const COUNTRY_CLASS: Record<HostCountry, string> = { US: 'vn-us', CA: 'vn-ca', MX: 'vn-mx' }

interface LabelTweak {
  dx?: number
  dy?: number
  anchor?: 'start' | 'end'
}

// hand-tuned label nudges to avoid overlaps / right-edge overflow (keyed by venue id)
const LABEL_TWEAK: Record<string, LabelTweak> = {
  '400248623': { dy: -8 }, // Boston: above-right, clears NY/NJ label
  '400257536': { dy: 4 }, // New Jersey: right of pin (room before map edge)
  '400248622': { anchor: 'end', dx: -11, dy: 9 }, // Philadelphia: left-below
  '400222084': { dy: 11 }, // Mexico City: nudged down, clears Guadalajara
  '400252150': { anchor: 'end', dx: -11 }, // Guadalajara: left over the ocean
  '400238450': { dy: -8 }, // Monterrey: above-right
}

